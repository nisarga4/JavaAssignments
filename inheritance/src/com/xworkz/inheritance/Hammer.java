package com.xworkz.inheritance;

public class Hammer {

        public void hit() {
            System.out.println("Hitting with the hammer...");
        }

        public void lift() {
            System.out.println("Lifting the hammer...");
        }

        public void drop() {
            System.out.println("Dropping the hammer...");
        }

        public void clean() {
            System.out.println("Cleaning the hammer...");
        }

        public void store() {
            System.out.println("Storing the hammer...");
        }
    }


