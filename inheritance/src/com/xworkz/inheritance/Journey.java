package com.xworkz.inheritance;

    public class Journey {
        public void begin() {
            System.out.println("Journey is beginning...");
        }

        public void travel() {
            System.out.println("Traveling through the journey...");
        }

        public void explore() {
            System.out.println("Exploring during the journey...");
        }

        public void faceChallenges() {
            System.out.println("Facing challenges in the journey...");
        }

        public void conclude() {
            System.out.println("Journey is concluding...");
        }
    }
