package com.xworkz.inheritance;


    public class Bottle {
        public void open() {
            System.out.println("Opening the bottle...");
        }

        public void close() {
            System.out.println("Closing the bottle...");
        }

        public void fill() {
            System.out.println("Filling the bottle...");
        }

        public void empty() {
            System.out.println("Emptying the bottle...");
        }

        public void shake() {
            System.out.println("Shaking the bottle...");
        }
    }

