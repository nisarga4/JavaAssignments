package com.xworkz.inheritance;

public class Jeans {
        public void wear() {
            System.out.println("Wearing jeans...");
        }

        public void wash() {
            System.out.println("Washing jeans...");
        }

        public void fold() {
            System.out.println("Folding the jeans...");
        }

        public void iron() {
            System.out.println("Ironing the jeans...");
        }

        public void hang() {
            System.out.println("Hanging the jeans...");
        }
    }
