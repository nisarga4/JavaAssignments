package com.xworkz.inheritance;

public class Pen {

        public void write() {
            System.out.println("Writing with the pen...");
        }

        public void openCap() {
            System.out.println("Opening the pen cap...");
        }

        public void closeCap() {
            System.out.println("Closing the pen cap...");
        }

        public void refillInk() {
            System.out.println("Refilling ink in the pen...");
        }

        public void store() {
            System.out.println("Storing the pen...");
        }
    }

