package com.xworkz.inheritance;

    public class Moon {
        public void rise() {
            System.out.println("Moon is rising...");
        }

        public void set() {
            System.out.println("Moon is setting...");
        }

        public void reflectSunlight() {
            System.out.println("Moon is reflecting sunlight...");
        }

        public void changePhases() {
            System.out.println("Moon is changing its phases...");
        }

        public void orbitEarth() {
            System.out.println("Moon is orbiting the Earth...");
        }
    }
