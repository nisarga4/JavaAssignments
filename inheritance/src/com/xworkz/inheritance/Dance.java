package com.xworkz.inheritance;
    public class Dance {
        public void start() {
            System.out.println("Starting the dance...");
        }

        public void perform() {
            System.out.println("Performing the dance...");
        }

        public void practice() {
            System.out.println("Practicing the dance...");
        }

        public void express() {
            System.out.println("Expressing emotions through dance...");
        }

        public void end() {
            System.out.println("Ending the dance...");
        }
    }