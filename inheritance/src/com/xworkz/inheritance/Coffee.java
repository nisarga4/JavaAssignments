package com.xworkz.inheritance;

public class Coffee {

        public void brew() {
            System.out.println("Brewing coffee...");
        }

        public void pour() {
            System.out.println("Pouring coffee into a mug...");
        }

        public void stir() {
            System.out.println("Stirring the coffee...");
        }

        public void serve() {
            System.out.println("Serving the coffee...");
        }

        public void drink() {
            System.out.println("Drinking the coffee...");
        }
    }

