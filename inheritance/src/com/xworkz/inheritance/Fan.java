package com.xworkz.inheritance;

public class Fan {
        public void turnOn() {
            System.out.println("Turning on the fan...");
        }

        public void turnOff() {
            System.out.println("Turning off the fan...");
        }

        public void increaseSpeed() {
            System.out.println("Increasing fan speed...");
        }

        public void decreaseSpeed() {
            System.out.println("Decreasing fan speed...");
        }

        public void rotate() {
            System.out.println("Rotating the fan...");
        }
    }


