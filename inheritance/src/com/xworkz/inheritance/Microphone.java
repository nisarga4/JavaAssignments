package com.xworkz.inheritance;

public class Microphone {
        public void powerOn() {
            System.out.println("Turning on the microphone...");
        }

        public void powerOff() {
            System.out.println("Turning off the microphone...");
        }

        public void record() {
            System.out.println("Recording audio...");
        }

        public void mute() {
            System.out.println("Muting the microphone...");
        }

        public void unmute() {
            System.out.println("Unmuting the microphone...");
        }
    }

