package com.xworkz.inheritance;
    public class Music {
        public void play() {
            System.out.println("Playing music...");
        }

        public void pause() {
            System.out.println("Pausing the music...");
        }

        public void stop() {
            System.out.println("Stopping the music...");
        }

        public void adjustVolume() {
            System.out.println("Adjusting the music volume...");
        }

        public void repeat() {
            System.out.println("Repeating the music...");
        }
    }