package com.xworkz.inheritance;

public class Baseball {

        public void pitch() {
            System.out.println("Pitching the baseball...");
        }

        public void catchBall() {
            System.out.println("Catching the baseball...");
        }

        public void throwBall() {
            System.out.println("Throwing the baseball...");
        }

        public void warmUp() {
            System.out.println("Warming up for the game...");
        }

        public void play() {
            System.out.println("Playing baseball...");
        }
    }