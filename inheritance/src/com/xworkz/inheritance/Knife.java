package com.xworkz.inheritance;

public class Knife {
        public void cut() {
            System.out.println("Cutting with the knife...");
        }

        public void sharpen() {
            System.out.println("Sharpening the knife...");
        }

        public void clean() {
            System.out.println("Cleaning the knife...");
        }

        public void store() {
            System.out.println("Storing the knife safely...");
        }

        public void inspectBlade() {
            System.out.println("Inspecting the blade of the knife...");
        }
    }

