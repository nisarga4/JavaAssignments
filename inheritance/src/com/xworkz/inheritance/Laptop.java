package com.xworkz.inheritance;

public class Laptop {
        public void powerOn() {
            System.out.println("Powering on the laptop...");
        }

        public void powerOff() {
            System.out.println("Powering off the laptop...");
        }

        public void runProgram() {
            System.out.println("Running a program on the laptop...");
        }

        public void charge() {
            System.out.println("Charging the laptop...");
        }

        public void shutdown() {
            System.out.println("Shutting down the laptop...");
        }
    }

