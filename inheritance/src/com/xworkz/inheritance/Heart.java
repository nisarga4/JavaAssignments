package com.xworkz.inheritance;
    public class Heart {
        public void beat() {
            System.out.println("Heart is beating...");
        }

        public void pumpBlood() {
            System.out.println("Heart is pumping blood...");
        }

        public void react() {
            System.out.println("Heart is reacting to emotions...");
        }

        public void feel() {
            System.out.println("Heart is feeling something...");
        }

        public void rest() {
            System.out.println("Heart is resting...");
        }
    }
