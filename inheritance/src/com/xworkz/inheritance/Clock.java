package com.xworkz.inheritance;

public class Clock {
        public void showTime() {
            System.out.println("Showing the current time...");
        }

        public void tick() {
            System.out.println("Clock is ticking...");
        }

        public void adjustTime() {
            System.out.println("Adjusting the time...");
        }

        public void stop() {
            System.out.println("Stopping the clock...");
        }

        public void start() {
            System.out.println("Starting the clock...");
        }
    }
