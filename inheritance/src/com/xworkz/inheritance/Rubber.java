public class Rubber {
        public void expand() {
            System.out.println("Rubber is expanding...");
        }

        public void bend() {
            System.out.println("Rubber is bending...");
        }

        public void stretch() {
            System.out.println("Rubber is stretching...");
        }

        public void melt() {
            System.out.println("Rubber is melting...");
        }

        public void bounce() {
            System.out.println("Rubber is bouncing...");
        }
    }
