package com.xworkz.inheritance;

    public class Eye {
        public void see() {
            System.out.println("Eye is seeing the world...");
        }

        public void blink() {
            System.out.println("Eye is blinking...");
        }

        public void focus() {
            System.out.println("Eye is focusing on an object...");
        }

        public void senseLight() {
            System.out.println("Eye is sensing light...");
        }

        public void tearUp() {
            System.out.println("Eye is tearing up...");
        }
    }

