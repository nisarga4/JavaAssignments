package com.xworkz.inheritance;


    public class Star {
        public void shine() {
            System.out.println("Star is shining brightly...");
        }

        public void twinkle() {
            System.out.println("Star is twinkling...");
        }

        public void emitLight() {
            System.out.println("Star is emitting light...");
        }

        public void burn() {
            System.out.println("Star is burning gases...");
        }

        public void stayInGalaxy() {
            System.out.println("Star is staying in the galaxy...");
        }
    }

