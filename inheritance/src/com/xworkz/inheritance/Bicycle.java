package com.xworkz.inheritance;

public class Bicycle {
        public void ride() {
            System.out.println("Riding the bicycle...");
        }

        public void brake() {
            System.out.println("Applying the bicycle brakes...");
        }

        public void pedal() {
            System.out.println("Pedaling the bicycle...");
        }

        public void adjustSeat() {
            System.out.println("Adjusting the bicycle seat...");
        }

        public void park() {
            System.out.println("Parking the bicycle...");
        }
    }

