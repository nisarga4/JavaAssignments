package com.xworkz.inheritance;


public class Lamp {
    public void turnOn() {
        System.out.println("Turning on the lamp...");
    }

    public void turnOff() {
        System.out.println("Turning off the lamp...");
    }

    public void checkBulb() {
        System.out.println("Checking the bulb of the lamp...");
    }

    public void clean() {
        System.out.println("Cleaning the lamp...");
    }

    public void moveToAnotherPlace() {
        System.out.println("Moving the lamp to another place...");
    }
}

