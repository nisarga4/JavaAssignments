public class Wallet {
        public void open() {
            System.out.println("Opening the wallet...");
        }

        public void close() {
            System.out.println("Closing the wallet...");
        }

        public void addCard() {
            System.out.println("Adding a card to the wallet...");
        }

        public void removeCard() {
            System.out.println("Removing a card from the wallet...");
        }

        public void checkMoney() {
            System.out.println("Checking money in the wallet...");
        }
    }
