package com.xworkz.inheritance;

public class Shoe {

        public void wear() {
            System.out.println("Wearing the shoes...");
        }

        public void remove() {
            System.out.println("Removing the shoes...");
        }

        public void clean() {
            System.out.println("Cleaning the shoes...");
        }

        public void polish() {
            System.out.println("Polishing the shoes...");
        }

        public void store() {
            System.out.println("Storing the shoes...");
        }
    }


