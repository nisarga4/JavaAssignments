package com.xworkz.Casting;

public class Bird {

        public void fly() {
            System.out.println("Bird is flying...");
        }

        public void sing() {
            System.out.println("Bird is singing...");
        }

        public void buildNest() {
            System.out.println("Bird is building a nest...");
        }

        public void eat() {
            System.out.println("Bird is eating seeds...");
        }

        public void sleep() {
            System.out.println("Bird is sleeping in the tree...");
        }
    }
